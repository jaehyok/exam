package Codingdojang;

public class CD408 {

    /*
     * 1차원의 점들이 주어졌을 때, 그 중 가장 거리가 짧은 것의 쌍을 출력하는 함수를 작성하시오. (단 점들의 배열은 모두
     * 정렬되어있다고 가정한다.) 예를들어 S={1, 3, 4, 8, 13, 17, 20} 이 주어졌다면, 결과값은 (3, 4)가 될
     * 것이다.
     * http://codingdojang.com/scode/408?answer_mode=hide
     */
    int[] S = { 1, 3, 4, 8, 13, 17, 20 };

    public void run() {
	int result = Integer.MAX_VALUE;
	int result_pos = -1;
	for (int i = 1; i < S.length; i++) {
	    int dleta = S[i] - S[i - 1];
	    if (dleta < result) {
		result = dleta;
		result_pos = i - 1;
		if(result == 0)
		    break;
	    }
	}

	System.out.println("(" + S[result_pos] + ", " + S[result_pos + 1] + ")");
    }

}
